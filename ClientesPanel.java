    private void BotaoAlterarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_BotaoAlterarActionPerformed
    {//GEN-HEADEREND:event_BotaoAlterarActionPerformed
        // TODO add your handling code here:
        if(control.ctrlClientes().validarCPF(TextoCpf.getText()))
        {
            TextoCpf.setEnabled(false);
            TextoNome.setEnabled(false);
            TextoNome.setText(control.ctrlClientes().getNomePessoas(TextoCpf.getText()));
            TextoEndereco.setText(control.ctrlClientes().getEnderecoPessoas(TextoCpf.getText()));
            TextoTelefone.setText(control.ctrlClientes().getTelefonePessoas(TextoCpf.getText()));
            TextoEmail.setText(control.ctrlClientes().getEmailPessoas(TextoCpf.getText()));
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Não existe Cliente com esse CPF");
        }
    }//GEN-LAST:event_BotaoAlterarActionPerformed

    private void BotaoSalvarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_BotaoSalvarActionPerformed
    {//GEN-HEADEREND:event_BotaoSalvarActionPerformed
        // TODO add your handling code here:
        TextoCpf.setEnabled(true);
        TextoNome.setEnabled(true);
        if((!"".equals(TextoCpf.getText()))&&(!"".equals(TextoEmail.getText()))&&(!"".equals(TextoNome.getText()))&&(!"".equals(TextoEndereco.getText()))&&(!"".equals(TextoTelefone.getText())))
        {
            control.ctrlClientes().AlterarPessoa(control.ctrlClientes().getPessoa(TextoCpf.getText()),TextoEmail.getText(),TextoEndereco.getText(),TextoTelefone.getText());
            JOptionPane.showMessageDialog(null,"Alterado com Sucesso!");
        }
        else
            JOptionPane.showMessageDialog(null,"Não foi possivel alterar pois há campos vazios!");

        TextoCpf.setText("");
        TextoEmail.setText("");
        TextoNome.setText("");
        TextoEndereco.setText("");
        TextoTelefone.setText("");
    }//GEN-LAST:event_BotaoSalvarActionPerformed

