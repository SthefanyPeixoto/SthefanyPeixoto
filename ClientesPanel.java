    private void BotaoCadastrarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_BotaoCadastrarActionPerformed
    {//GEN-HEADEREND:event_BotaoCadastrarActionPerformed
        if((!"".equals(TextoCpf.getText()))&&(!"".equals(TextoEmail.getText()))&&(!"".equals(TextoNome.getText()))&&(!"".equals(TextoEndereco.getText()))&&(!"".equals(TextoTelefone.getText())))
        {
            if(control.ctrlClientes().CadastrarPessoa(TextoCpf.getText( ),TextoEmail.getText(),TextoNome.getText(),TextoEndereco.getText(),TextoTelefone.getText()))
            {
                atualizarLista();
                JOptionPane.showMessageDialog(null,"Cadastrado com Sucesso!");
            }

            else
            {
                System.out.println("Erro ao cadastrar");
                JOptionPane.showMessageDialog(null,"Ja existe Cliente com esse CPF");
            }
        }
        else
            JOptionPane.showMessageDialog(null,"Não foi possivel Cadastar, pois há campos vazios!");
        
        TextoCpf.setText("");
        TextoEmail.setText("");
        TextoNome.setText("");
        TextoEndereco.setText("");
        TextoTelefone.setText("");
    }//GEN-LAST:event_BotaoCadastrarActionPerformed

